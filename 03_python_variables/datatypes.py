'''Data Types

A data type defines what kind of value a variable can store and what operations you can perform on it.

Python has the following built-in data types:

int → whole numbers (e.g., 10, -5)

float → decimal numbers (e.g., 3.14, -0.001)

str → text (in quotes)

bool → True/False

list → ordered, mutable collection → [1,2,3]

tuple → ordered, immutable collection → (1,2,3)

set → unique, unordered collection → {1,2,3}

dict → key-value pairs → {"name":"Harry", "age":20}'''

age = 3 
print(age)
print(type(age))

cgpa = 8.2
print(cgpa)
print(type(cgpa))

name = "Harry"
print(name)
print(type(name))

is_completed = True # can also be False
print(is_completed)
print(type(is_completed))


