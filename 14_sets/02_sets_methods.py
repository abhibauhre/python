num = {23, 45, 67, 89, 23, 45}
print(num) # its print unique values only because set dont support duplicate values
print(num.add(99)) # its add 99 to set
print(num)
print(num.update([1,2,3,4,5])) # its add multiple values to set
print(num)
print(num.remove(3)) # its remove value 3 from set
print(num)
print(num.pop()) # its remove random value from set
print(num)  # its remove random value from set
print(num.clear()) # its clear the set
print(num) # its print empty set