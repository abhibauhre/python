# A string in Python is an immutable sequence of characters (letters, digits, symbols, spaces). You create a string by enclosing text in single ('...') or double ("...") quotes (or triple quotes for multi-line).

# Key ideas:

# Immutable: once created, you cannot change characters in-place (you can create a new string).

# Sequence: supports indexing, slicing, iteration, and length (len()).

# Concatenation: you can join strings with +.

# Repetition: repeat with * operator.

# Escaping: use \ for special characters (\n, \', \", \\, \t).

# Raw strings: prefix r"..." to ignore escape sequences (useful for file paths, regex).

# Formatting: insert variables into strings using f-strings (recommended), .format(), or % formatting.

# Methods: strings provide many helpful methods (.lower(), .upper(), .split(), .join(), .replace(), .find(), .strip(), etc.).

# Because strings are sequences, everything you know about sequence operations (iteration, in operator, indexing, slicing) applies.